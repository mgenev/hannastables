// Database
            function initDatabase(){ // Init/update database content.
                global $wpdb;
                    
                $backendData = $this->backendData();
                $frontendData = $this->frontendData();
                
                for ($i=0; $i<count($backendData); $i++){
                    $translation = $wpdb->get_row('SELECT * FROM '.DOPBSP_Translation_table.'_en WHERE key_data="'.$backendData[$i]['key'].'" AND location="backend"');

                    if ($wpdb->num_rows == 0){
                        $backendData[$i]['add'] = true;
                    }
                    else{
                        $backendData[$i]['add'] = false;
                    }
                }

                for ($i=0; $i<count($frontendData); $i++){
                    $translation = $wpdb->get_row('SELECT * FROM '.DOPBSP_Translation_table.'_en WHERE key_data="'.$frontendData[$i]['key'].'" AND location="frontend"');

                    if ($wpdb->num_rows == 0){
                        $frontendData[$i]['add'] = true;
                    }
                    else{
                        $frontendData[$i]['add'] = false;
                    }
                }

                for ($l=0; $l<count($this->languages_prefix); $l++){
                    $query_values = array();

                    for ($i=0; $i<count($backendData); $i++){
                        if ($backendData[$i]['add']){
                            array_push($query_values, '(\''.$backendData[$i]['key'].'\', \'backend\', \''.$i.'\', \''.$backendData[$i]['parent'].'\', \''.$backendData[$i]['text'].'\', \''.(isset($backendData[$i][$this->languages_prefix[$l]]) ? $backendData[$i][$this->languages_prefix[$l]]:$backendData[$i]['text']).'\')');
                        }
                    }

                    for ($i=0; $i<count($frontendData); $i++){
                        if ($frontendData[$i]['add']){
                            array_push($query_values, '(\''.$frontendData[$i]['key'].'\', \'frontend\', \''.$i.'\', \''.$frontendData[$i]['parent'].'\', \''.$frontendData[$i]['text'].'\', \''.(isset($frontendData[$i][$this->languages_prefix[$l]]) ? $frontendData[$i][$this->languages_prefix[$l]]:$frontendData[$i]['text']).'\')');
                        }
                    }

                    if (count($query_values) > 0){
                        $wpdb->query('INSERT INTO '.DOPBSP_Translation_table.'_'.$this->languages_prefix[$l].' (key_data, location, position, parent_key, text_data, translation) VALUES '.implode(', ', $query_values));
                    }
                }
            }
            
            function resetDatabase($ajax_request = true){ // Reset translation in database.
                global $wpdb;
                
                for ($l=0; $l<count($this->languages_prefix); $l++){
                    $wpdb->query('TRUNCATE '.DOPBSP_Translation_table.'_'.$this->languages_prefix[$l]);
                }
                $this->initDatabase();
                
                if ($ajax_request){
                    echo '';

                    die();
                }
            }
            
// Set translation            
            function setTranslation($location = 'backend', $language = DOPBSP_CONFIG_FRONTEND_DEFAULT_LANGUAGE){ // Set translation constants.
                global $wpdb;
                
                if ($location == 'backend'){
                    if (!is_network_admin()){
                        $language = get_option('DOPBSP_backend_language_'.wp_get_current_user()->ID);

                        if ($language == ''){
                            $language = DOPBSP_CONFIG_BACKEND_DEFAULT_LANGUAGE;
                            add_option('DOPBSP_backend_language_'.wp_get_current_user()->ID, DOPBSP_CONFIG_BACKEND_DEFAULT_LANGUAGE);
                        }
                    }
                    else{
                        $language = DOPBSP_CONFIG_BACKEND_DEFAULT_LANGUAGE;
                    }
                }
                
                $translation = $wpdb->get_results('SELECT * FROM '.DOPBSP_Translation_table.'_'.$language.' WHERE location="'.$location.'"');
                
                foreach ($translation as $item){
                    if (!defined($item->key_data)){
                        define($item->key_data, str_replace('<<single-quote>>', "'", stripslashes($item->translation)));
                    }
                }
            }
            
// Show Translation 
            function showTranslation(){// Show Calendars List.
                global $wpdb;
                $translationHTML = array();
                
                $location = $_POST['location'];
                $language = $_POST['language'];
                
                $translation = $wpdb->get_results('SELECT * FROM '.DOPBSP_Translation_table.'_'.$language.' WHERE location="'.$location.'" AND parent_key<>"" ORDER BY id, parent_key ASC');
                
                array_push($translationHTML, '<table>');
                array_push($translationHTML, '  <tbody>');
                
                foreach ($translation as $item){
                    array_push($translationHTML, '<tr>');
                    array_push($translationHTML, '  <td>');
                    array_push($translationHTML, ($item->parent_key == 'none' ? '':'<span class="translation-hint">['.constant($item->parent_key).']</span>').' '.str_replace('<<single-quote>>', "'", stripslashes($item->text_data)));
                    array_push($translationHTML, '  </td>');
                    array_push($translationHTML, '  <td>');
                    array_push($translationHTML, '      <textarea name="DOPBSP-translation'.$item->id.'" id="DOPBSP-translation'.$item->id.'" rows="1" cols="" onkeyup="dopbspEditTranslation('.$item->id.', \''.$language.'\', this.value)" onblur="dopbspEditTranslation('.$item->id.', \''.$language.'\', this.value, true)">'.str_replace('<<single-quote>>', "'", stripslashes($item->translation)).'</textarea>');
                    array_push($translationHTML, '      <span class="translation-loader" id="DOPBSP-translation-loader'.$item->id.'"></span>');
                    array_push($translationHTML, '  </td>');
                    array_push($translationHTML, '</tr>');

                }
                
                array_push($translationHTML, '  </tbody>');
                array_push($translationHTML, '</table>');
                
                echo implode('', $translationHTML);
                
            	die();                
            }
            
